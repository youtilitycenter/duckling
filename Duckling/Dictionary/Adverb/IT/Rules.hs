searchAdverb :: (Eq a) => a -> [(a,b)] -> [b]
searchAdverb x = map snd . filter ((==x).fst)

ruleNumeralQuantities :: [Rule]
ruleNumeralQuantities = map go adverbs
  where
    go :: (Text, String) -> Rule
    go (name, regexPattern) = Rule
      { name = name
      , pattern = [regex regexPattern]
      , prod = \case
        (Token RegexMatch (GroupMatch (match:_)):
         _) -> Just . Token Adverb $ adverbHelper name
        _ -> Nothing
      }

rules :: [Rule]
rules =
  [
  ]
  ++ ruleNumeralQuantities
